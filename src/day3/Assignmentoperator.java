package day3;

public class Assignmentoperator {

	public static void main(String[] args) 
	{
		// assignment  operators = += -= *= /=
		
		int a=10;
	
		a++; //a=a+5;
		System.out.println(a);
		
		
		
		a--;
		System.out.println(a);
		
		a*=5;
		System.out.println(a);
		
		a/=2;
		System.out.println(a);
		
		a%=2;
		System.out.println(a);
		
    
	}

}
