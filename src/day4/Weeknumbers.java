package day4;

public class Weeknumbers 
{

	public static void main(String[] args) 
	{
		
		String week_names="tuesday";
		
		switch(week_names)
		{
		
		case "sunday" : System.out.println(1); break;
		
		case "monday" : System.out.println(2); break;
		
		case "tuesday" : System.out.println(3); break;
		
		case "wednsday" : System.out.println(4); break;
		
		case "thersday" : System.out.println(5); break;
		
		case "friday" : System.out.println(6); break;
		
		case "satraday" : System.out.println(7); break;
		
		default : System.out.println("invalid num");		}
		

	}

}
